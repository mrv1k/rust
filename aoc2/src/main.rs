use std::{collections::HashMap, ops::Deref};

fn main() {
    // std::env::set_var("RUST_BACKTRACE", "1");
    // let stub = r#"A Y
    //     B X
    //     C Z
    //     A Z
    //     B Z
    //     C Z
    //     A X
    //     B X
    //     C X"#;
    // let bg = stub.split("\n").map(|s| s.trim());
    let bg = input().split("\n");

    let score = HashMap::from([
        ("A", 1), // rock
        ("B", 2), // paper
        ("C", 3), // scissors
        ("X", 1), // rock
        ("Y", 2), // paper
        ("Z", 3), // scissors
    ]);

    let lost = 0;
    let draw = 3;
    let won = 6;

    let mut strategy = 0;

    for pvp in bg {
        let opponents = pvp.split_whitespace();

        let round: Vec<&i32> = opponents.clone().map(|p| score.get(p).unwrap()).collect();
        let you = *round.first().unwrap().deref();
        // let me = round.last().unwrap().deref();

        let how_to_end = opponents.last().unwrap();
        let lose = how_to_end == "X";
        let win = how_to_end == "Z";

        print!("{:?}", round);
        let me = if win {
            // (you + 1) % 3
            if you == 2 {
                3
            } else if you == 3 {
                1
            } else {
                2
            }
        } else if lose {
            if you == 2 {
                1
            } else if you == 3 {
                2
            } else {
                3
            }
        } else {
            you
        };

        let result = you - me;

        let score = if result == 2 || result == -1 {
            me + won
        } else if result == -2 || result == 1 {
            me + lost
        } else {
            me + draw
        };
        strategy += score;
        println!(
            "you {:#?} | me {:#?} = result {:#?} | score {:#?}",
            you, me, result, score
        );
    }

    println!("{}", strategy);
}

